async function main() {
  console.log('Starting Flawless autograph print run corrections...\n');

  let totalSetsUpdated = 0;
  let totalCardsUpdated = 0;

  for (const [setName, cardCorrections] of Object.entries(CARD_CORRECTIONS)) {
    const set = await prisma.set.findFirst({
      where: {
        name: setName,
        release: { slug: RELEASE_SLUG }
      },
      include: { cards: true }
    });

    if (!set) {
      console.log(`⚠️  Set not found: ${setName}`);
      continue;
    }

    console.log(`Processing: ${setName} (${set.cards.length} cards)`);

    // Check if print runs vary - if so, set-level should be null
    const printRuns = Object.values(cardCorrections);
    const uniquePrintRuns = [...new Set(printRuns)];
    const setPrintRun = uniquePrintRuns.length === 1 ? uniquePrintRuns[0] : null;

    // Update set print run if it varies
    if (set.printRun !== setPrintRun) {
      await prisma.set.update({
        where: { id: set.id },
        data: { printRun: setPrintRun }
      });
      console.log(`  Set printRun: ${set.printRun} → ${setPrintRun || 'variable'}`);
    }

    // Update each card
    let cardsUpdated = 0;
    for (const card of set.cards) {
      const correctPrintRun = cardCorrections[card.cardNumber];
      if (correctPrintRun && correctPrintRun !== card.printRun) {
        const numbered = correctPrintRun === 1 ? '1 of 1' : `/${correctPrintRun}`;

        // Fix slug - remove old print run, add new one
        let newSlug = card.slug;
        // Remove existing print run suffix
        newSlug = newSlug.replace(/-\d+$/, '').replace(/-1-of-1$/, '');
        // Add correct print run
        newSlug = correctPrintRun === 1 ? `${newSlug}-1-of-1` : `${newSlug}-${correctPrintRun}`;

        await prisma.card.update({
          where: { id: card.id },
          data: {
            printRun: correctPrintRun,
            numbered: numbered,
            slug: newSlug
          }
        });
        cardsUpdated++;
      }
    }

    if (cardsUpdated > 0) {
      console.log(`  Updated ${cardsUpdated} cards`);
      totalCardsUpdated += cardsUpdated;
      totalSetsUpdated++;
    }
  }

  console.log('\n========================================');
  console.log('Corrections Complete!');
  console.log('========================================');
  console.log(`Sets updated: ${totalSetsUpdated}`);
  console.log(`Cards updated: ${totalCardsUpdated}`);
  console.log('========================================\n');
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
